-- create tables
create table d171_dipendente (
    id                             number generated by default on null as identity 
                                   constraint d171_dipendente_id_pk primary key,
    dipendente                     varchar2(9 char)
                                   constraint d171_dipendente_dipendente_unq unique not null,
    tariffa                        number not null
)
;

create table d171_cliente (
    id                             number generated by default on null as identity 
                                   constraint d171_cliente_id_pk primary key,
    cliente                        varchar2(10 char)
                                   constraint d171_cliente_cliente_unq unique not null
)
;

create table d171_commessa (
    id                             number generated by default on null as identity 
                                   constraint d171_commessa_id_pk primary key,
    commessa                       varchar2(6 char)
                                   constraint d171_commessa_commessa_unq unique not null
)
;

create table d171_attivita (
    id                             number generated by default on null as identity 
                                   constraint d171_attivita_id_pk primary key,
    dipendente_id                  number
                                   constraint d171_attivita_dipendente_id_fk
                                   references d171_dipendente not null,
    cliente_id                     number
                                   constraint d171_attivita_cliente_id_fk
                                   references d171_cliente not null,
    commessa_id                    number
                                   constraint d171_attivita_commessa_id_fk
                                   references d171_commessa not null,
    data                           date not null,
    ore                            number not null
)
;

-- table index
create index d171_attivita_i1 on d171_attivita (cliente_id);
create index d171_attivita_i112 on d171_attivita (commessa_id);
create index d171_attivita_i123 on d171_attivita (dipendente_id);

